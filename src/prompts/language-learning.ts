export const languageLearningPrompt = (
  lyrics: string,
  songName?: string,
  artist?: string
) => `
我将提供一段歌词文本，请作为语言学习专家，分析歌词中的词汇和语法，帮助学习者理解和学习歌词的原语言。

**请按照以下维度进行语言学习分析：**

**一、词汇分析**
* **核心词汇：** 列出歌词中最重要的 10-15 个词汇，包括：
  - 原文词汇
  - 发音/读音（如适用）
  - 中文释义
  - 词性
  - 使用场景或例句

* **高频词汇：** 指出歌词中重复出现的词汇，分析其在歌词中的作用

**二、语法结构分析**
* **句型结构：** 分析歌词中出现的主要句型结构（如时态、语态、从句等）
* **语法要点：** 列出 3-5 个值得学习的语法点，并举例说明
* **特殊表达：** 指出歌词中的特殊语法现象或口语化表达

**三、惯用表达与文化**
* **习惯用语：** 列出歌词中的成语、俗语、惯用表达
* **文化背景：** 解释与语言相关的文化背景知识
* **修辞手法：** 分析歌词中使用的修辞手法（比喻、拟人等）

**四、学习建议**
* **难度评估：** 评估这首歌词对语言学习者的难度（初级/中级/高级）
* **学习重点：** 建议学习者重点关注的语言点
* **记忆技巧：** 提供帮助记忆词汇和表达的技巧

**五、精选片段深度解析**
* 选择歌词中 1-2 个最具代表性的句子或段落
* 进行逐词逐句的详细解析
* 包括词汇、语法、语义的完整分析

---

**歌曲信息：** ${songName ? `歌名：${songName}` : ""}${
  artist ? ` / 艺术家：${artist}` : ""
}

**歌词文本：**
${lyrics}

**注意事项：**
- 请根据歌词的实际语言进行分析（可能是中文、英文、日文、韩文等）
- 如果歌词包含多种语言，请分别分析
- 专注于语言学习角度，而非音乐赏析
- 使用清晰的格式，便于学习者查阅和学习
`;
